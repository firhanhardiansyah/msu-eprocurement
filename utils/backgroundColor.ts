export function getBackgroundColor(index: number): string {
  const colors = [
    "bg-rose-500",
    "bg-orange-500",
    "bg-amber-500",
    "bg-lime-500",
    "bg-teal-500",
    "bg-sky-500",
    "bg-blue-500",
    "bg-purple-500",
    "bg-pink-500",
    "bg-green-500",
    "bg-red-500",
  ];

  return colors[index];
}
